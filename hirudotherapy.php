            <p>Данному методу несколько тысяч лет. Расцвет гирудотерапии приходится на Средние века, тогда же произошла и первая дискредитация метода. Кровоизвлечение стало считаться панацеей от всех болезней и методики дошли до фанатизма: были случаи, когда использовались до сотни пиявок на один сеанс.  Неправильное понимание и простое незнание механизмов действия пиявок, а также их широкое использование непрофессионалами (не имеющего прямого отношения к медицине) породило множество мифов. Кто-то считал пиявок волшебным средством, кто-то шарлатанством.
  В конце XIX – начале XX века в Европе начался расцвет фармакологии. Стали синтезироваться десятки и сотни новых препаратов. К этому времени, уже были открыты микроорганизмы, и господствовала инфекционная теория развития болезней. Лечение с помощью кровопусканий отошло на второй план.
  Россия, как известно уникальная страна. Уникальна она и в плане традиций гирудотерапии. Практически только в России гирудотерапевтические знания передавались из поколения в поколение, от отца к сыну. У нас до сих пор существуют врачи, которые учились не по книгам, а являются потомственными гирудотерапевтами. Поэтому наша гирудотерапевтическая школа одна из сильнейших в мире. Да и во многих деревнях и сегодня живут бабушки, которые получили знания еще от своих бабушек. Практические знания донесены до сегодняшних дней только по одной причине – они действительно работают.
            </p>
            <p>А что же в остальном мире? Если говорить о Востоке (Китай, Индия), там о пиявках тоже всегда знали и никогда не забывали о них. Но у них на первом плане свои традиционные методики оздоровления (иглоукалывание, аюрведа и пр), поэтому гирудотерапия там всего лишь одно из средств в огромной системе оздоровления. </p>
        
          
          <blockquote><p>Если говорить о Западе, сразу отметим – на сегодняшний день там вновь огромный интерес к гирудотерапии.  Есть несколько направлений, где скромные пиявки заняли прочные позиции.     Например, в пластической хирургии (кожные лоскуты гораздо лучше приживаются при использовании пиявок).  Немецкие медики доказали эффективность гирудотерапии  при артрозах и широко используют данный метод.</p></blockquote>
          
           <p>Как пиявки лечат?
Сразу скажем, что мы не разделяем мифы об «энергетическом эффекте» пиявок. Отрицательно относимся  к «открытию чакр» и прочим сомнительным методикам.
У гирудотерапии есть несколько вполне материальных, научно-обоснованных  механизмов действия, а именно:
   Противовоспалительное действие. Оно обусловлено определенными веществами в составе слюны (в слюне пиявки определяется несколько десятков биологически активных веществ!).  Существует и, так называемая, противовоспалительная фракция.
            </p>
            
            <p>Еще одна фракция – литическая («расплавляющая») обеспечивает локальное «расплавление» тканей и доступ пиявки к сосудам, лежащим в глубине кожи. Она же крайне полезна при «расплавлении» спаек и рубцов в брюшной полости, малом тазу. Есть ряд методик, позволяющий восстановить проходимость маточных труб после воспалительного процесса. Таким образом, в ряде случаев, пиявка может заменить скальпель  хирурга. Вот вам и маленький червячок!
  Улучшение микроциркуляции. В месте приставки пиявки открывается капиллярная сеть, улучшается доставка кислорода тканям, происходит лифмодренаж и удаление продуктов распада. Проще говоря, улучшается питание тканей: увеличивается количество кислорода и удаляются шлаки и токсины. Кроме этого, значительно увеличивается и доставка лекарственных препаратов непосредственно в проблемную область.
            </p>
          
          
         
          <p>
          	<img class="pull-left"  alt="" src="images/blog/hirudo-2.jpg">
            Улучшение местных иммунных процессов. Отчасти, это является следствием предыдущего пункта (при обеспечении нормальных условий жизнедеятельности тканей и клеток, данные  ткани становятся более «устойчивыми» к повреждающим тканям). Отчасти, это происходит при воздействии на лимфатическую систему, в которой, как известно, и находятся иммунокомпетентные клетки. Кроме того, существуют и более тонкие механизмы, обеспечивающие повышение местного иммунитета.
Дозированное кровоизвлечение обеспечивает целый ряд лечебных эффектов. Практически при любом воспалении возникает венозный застой. Образно говоря, микрососуды наших тканей из рек и ручейков превращаются в некие «болотца». Восстановление возможно только при условии «осушения» и разгрузке микроциркуляторного русла. Пиявки в этом случае являются идеальным средством.
   Кроме того, дозированная кровопотеря является мощным стимулятором защитных сил организма. Ни один фармакологический препарат не обладает таким выраженным эффектом.
          </p>
          <p>Рефлекторный компонент (по аналогии с иглоукалыванием): происходит повреждение кожных покровов в определенных точках, что вызывает последующую реакцию организма (в частности, активацию гипоталамо-гипофизарно-надпочечниковой системы).
            </p>
            <blockquote><p>
Можно ли сказать одной фразой, с помощью чего лечит пиявка? Да, можно. Назовем это повышением защитных сил организма, увеличением его ресурсов.  Отметим, что почти все фармакологические препараты действуют против защитных реакций организма (давление повысилось - снизим, кислотность желудочного сока высокая – блокируем, воспаление – подавим, и так далее). И только гирудотерапия действует синхронно с собственными защитными силами (например, артериальное давление после сеансов выравнивается не благодаря подавлению, а благодаря восстановлению баланса гипофизарно-надпочечниковой и вегетативной нервной системы).</p></blockquote>
      <p><img alt="" class="pull-right" src="images/blog/hirudo-3.jpg">Мы перечислили основные (но далеко не все) механизмы действия пиявок. Надеемся, что читателю теперь понятно, что правильно проведенный сеанс гирудотерапии обладает вполне конкретными лечебными эффектами (не имеющими никакого отношения ни к «биоэнергетической коррекции», ни к «самовнушению»).  

      Исходя из универсальных механизмом действия пиявок становится понятно, почему они показаны при огромном количестве заболеваний. В самом деле, при лечении какого заболевания не нужна активация защитных сил организма? Или улучшение микроциркуляции? Или противовоспалительное действие? Поэтому спектр заболеваний чрезвычайно широк.
       Начнем с болезней, которые без гирудотерапии лечить очень сложно (и практически невозможно):
Варикозная болезнь нижних конечностей и геморрой. До тех пор, пока не уберем излишний объем крови («застой») все лечебные мероприятия (включая операцию) будут давать лишь временный эффект;
«Синдром сгущения крови» (эритремия, высокие значения гемоглобина, тромбоцитов). Здесь точка приложения гирудотерапии четко видна и не требует объяснений.

Заболевания, при которых гирудотерапия чрезвычайно показана:
<ol>
  <li>Практически все основные кардиологические болезни:
    <ul>
      <li>• гипертоническая болезнь, ишемическая болезнь сердца, кардиалгии (боли в сердце)</li>
      <li>• Проявления венозной недостаточности и нарушений лимфодинамики в конечностях.</li>
    </ul>
  </li>
  <li>Неврологические: 
    <ul>
      <li>• обострение радикулита (корешковый синдром), компрессионные синдромы , невропатии и невралгии.</li>
    </ul>
  </li>
  <li>Кожные заболевания:
    <ul>
      <li>• Воспалительные заболевания кожи и подкожной клетчатки (инфильтраты, абсцессы, фурункулы).</li>
      <li>• Хронические и незаживающие язвы, последствия травм.</li>
      <li>• Герпетические высыпания, себорея, псориаз, экзема. </li>
    </ul>
  </li>
  <li>Воспалительные заболевания глаз, полости рта и ЛОР-органов</li>    
  <li>Профилактические курсы при планировании беременности</li>    
  <li>Артрозы и артриты в стадии обострения.</li>    
  <li>Хронические воспалительные заболевания, сопровождающиеся снижением местного иммунитета.</li>    
</ol>

</p>
            <p>
            	 <img alt="" src="images/blog/hirudo-4.jpg" class="margin-bottom-30 img-responsive">
                 С этими проблемами наиболее часто приходят на прием гирудотерапевта, список можно продолжать. Зная механизмы действия пиявок, становится понятным, что весь этот список не выдуман, а имеет под собой конкретную почву. С другой стороны, предостерегаем от другой крайности: пиявка не является волшебным средством, исцеляющим все болезни. В каждом конкретном случае эффект лечения определяется индивидуально и является результатом множества факторов.
            </p>

            
            
